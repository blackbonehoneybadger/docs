def heading(t): return P(t, "h1")
def sub(t): return P(t, "h2")

def bullets(items):
    return ListFlowable([ListItem(P(i, "li"), leftIndent=6) for i in items],
                        bulletType="bullet", start="•", bulletColor=GOLD,
                        leftIndent=12, bulletFontSize=8)

def callout(html, kind="box"):
    border = GOLD if kind == "box" else (OKBORDER if kind == "ok" else WARNBORDER)
    bg = CREAM if kind != "warn" else WARNBG
    t = Table([[P(html)]], colWidths=[170*mm])
    t.setStyle(TableStyle([
        ("BACKGROUND", (0,0), (-1,-1), bg),
        ("LINEBEFORE", (0,0), (0,-1), 3, border),
        ("BOX", (0,0), (-1,-1), 0.5, LINE),
        ("LEFTPADDING", (0,0), (-1,-1), 9),
        ("RIGHTPADDING", (0,0), (-1,-1), 9),
        ("TOPPADDING", (0,0), (-1,-1), 6),
        ("BOTTOMPADDING", (0,0), (-1,-1), 6),
    ]))
    return t

def dtable(rows, widths, header=True):
    data = []
    for r in rows:
        data.append([P(c, "cellb") if (header and ri==0) else P(c, "cell")
                     for ci, c in enumerate(r)] if False else
                    [P(c, "cellb" if (header and rows.index(r)==0) else "cell") for c in r])
    # build cleanly
    data = []
    for i, r in enumerate(rows):
        styley = "cellb" if (header and i == 0) else "cell"
        data.append([P(c, styley) for c in r])
    t = Table(data, colWidths=widths, repeatRows=1 if header else 0)
    ts = [
        ("GRID", (0,0), (-1,-1), 0.5, LINE),
        ("VALIGN", (0,0), (-1,-1), "TOP"),
        ("LEFTPADDING", (0,0), (-1,-1), 6),
        ("RIGHTPADDING", (0,0), (-1,-1), 6),
        ("TOPPADDING", (0,0), (-1,-1), 4),
        ("BOTTOMPADDING", (0,0), (-1,-1), 4),
    ]
    if header:
        ts += [("BACKGROUND", (0,0), (-1,0), ESPRESSO)]
    for i in range(1, len(rows)):
        if i % 2 == 0:
            ts.append(("BACKGROUND", (0,i), (-1,i), CREAM))
    t.setStyle(TableStyle(ts))
    return t

